q = int(input())

for test_case in range(1, q + 1):
    numbers = int(input())

    _50000_ = numbers // 50000
    _10000_ = (numbers % 50000) // 10000
    _5000_ = (numbers % 50000) % 10000 // 5000
    _1000_ = (numbers % 50000) % 10000 % 5000 // 1000
    _500_ = (numbers % 50000) % 10000 % 5000 % 1000 // 500
    _100_ = numbers % 50000 % 10000 % 5000 % 1000 % 500 // 100
    _50_ = numbers % 50000 % 10000 % 5000 % 1000 % 500 % 100 // 50
    _10_ = numbers % 50000 % 10000 % 5000 % 1000 % 500 % 100 % 50 // 10

    print('#{}'.format(test_case))
    print('{} {} {} {} {} {} {} {}'.format(_50000_, _10000_, _5000_, _1000_, _500_, _100_, _50_, _10_))
